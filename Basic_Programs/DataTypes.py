# Data types - I will come back and add to this file if I encounter a new data type

# String
strVar = "Hello, World!"
print("strVar:", strVar)
print(type(strVar), "\n")


# int
intVar = 12
print("intVar:", intVar)
print(type(intVar), "\n")


# float
floatVar = 3.14
print("floatVar:", floatVar)
print(type(floatVar), "\n")


# complex
complexVar = 2j
print("complexVar:", complexVar)
print(type(complexVar), "\n")


# boolean
booleanVar = True
print("booleanVar:", booleanVar)
print(type(booleanVar), "\n")


# list
listVar = [1, 2, 3]
print("listVar:", listVar)
print(type(listVar), "\n")


# tuple (immutable list like using final in Java)
tupleVar = (1, 2, 3)
print("tupleVar:", tupleVar)
print(type(tupleVar), "\n")


# set (unordered collection like HashSet in Java)
setVar = {1, 2, 3}
print("setVar:", setVar)
print(type(setVar), "\n")


# dictionary (key/values like HashMap in Java)
dictVar = {"Name": "Hulkenberg", "Hungry?": "Always"}
print("dictVar:", dictVar)
print(type(dictVar), "\n")

# nothing
noneVar = None
print("noneVar:", noneVar)
print(type(noneVar))





